class HarryAge {
    public static void main(String[] args) {
        // Variables for birth year and current year
        int birthYear = 2000;
        int currentYear = 2024;
        
        // Compute age
        int age = currentYear - birthYear;
        
        // Display output
        System.out.println("Harry's age in " + currentYear + " is " + age);
    }
}
